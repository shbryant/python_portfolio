def pokemon():
    while True:
        global day
        global level
        global wins
        global losses
        global mood
        activity = input("Welcome to Pokemon! This is day " + str(day) + "! Would you like to train your Pokemon, participate in a gym battle, test your odds at the final boss, end the game or rest?")
        if activity == "train" or activity == "Train":
            print("You did five push-upsand gained XP!")
            level = level + 1
            print("Your pokemon goes to bed. This is the end of day " + str(day))
            day = day + 1
            mood = mood + 5
        # Info screen
        elif activity == "rest" or activity == "Rest":
            print("Sweet dreams! This is the end of day " + str(day))
            print("Total victories: " + str(wins))
            print("Total losses: " + str(losses))
            if mood >=50:
                print("Current mood: Joyful!")
            else:
                print("Current mood: Devastated...")
            day = day + 1

            if level <= 10:
                gastly()
                print("Your pokemon, Gastly, is level " + str(level))
            elif level <=20:
                haunter()
                print("Your pokemon, Haunter, is level " + str(level))
            else:
                gengar()
                print("Your pokemon, Gengar, is level " + str(level))
        elif activity == "gym battle":
            odds = random.randint(0, 10)
            if mood >=50:
                if odds >= 4:
                    print("Great fighting! You launched a fireball and won your battle!")
                    level = level + 2
                    wins = wins + 1
                    mood = mood + 5
                else:
                    print("You suffered a devious blow to the head. You lose :(")
                    losses = losses + 1
                    mood = mood - 5
            else:
                if odds >= 6:
                    print("Amazing technique! You pulled out your sword and won your battle!")
                    level = level + 2
                    wins = wins + 1
                    mood = mood + 5
                else:
                    print("You were kicked against the wall and lost :(")
                    losses = losses + 1
                    mood = mood - 5
        elif activity == "final boss":
            if level <= 10:
                boss = random.randint(0,10)
                if boss == 5:
                    print("It was a tough battle... but in the end... Victory!!!!!! You beat the final boss!")
                    wins = wins + 1
                    print("Total victories: " + str(wins))
                    print("Total losses: " + str(losses))
                    break
                else:
                    print("You fought hard, but your opponent was just too good. You lose :(")
                    losses = losses+1
                    print("Total victories: " + str(wins))
                    print("Total losses: " + str(losses))
                    break
            elif level<= 20:
                boss = random.randint(0,5)
                if boss == 3 or boss == 4:
                    print("After a long and hard battle... Victory!! You beat the final boss!")
                    wins = wins + 1
                    print("Total victories: " +str(wins))
                    print("Total losses: " + str(losses))
                    break
                else:
                    print("You battled, but the sword was too sharp. You lose..")
                    losses = losses + 1
                    print("Total victories: " + str(wins))
                    print("Total losses: " + str(losses))
                    break
            elif level<= 25:
                boss = random.randint(0,3)
                if boss == 3 or boss ==2:
                    print("Your mastery of fighting is impeccable! Victory!! You beat the final boss!")
                    wins = wins + 1
                    print("Total victories: " + str(wins))
                    print("Total losses: " + str(losses))
                    break
                else:
                    print("You lose..")
                    losses = losses + 1
                    print("Total victories: " + str(wins))
                    print("Total losses: " + str(losses))
                    break
        elif activity == "end the game" or "end":
            print ("Goodbye!")
            print("Total victories: " + str(wins))
            print("Total losses: " + str(losses))
            break

